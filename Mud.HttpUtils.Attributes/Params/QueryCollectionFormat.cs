namespace Mud.HttpUtils.Attributes;

/// <summary>
/// 指定集合类型查询参数的序列化格式。
/// </summary>
/// <remarks>
/// 控制数组/集合参数在查询字符串中的格式化方式。
/// </remarks>
public enum QueryCollectionFormat
{
    /// <summary>
    /// 多参数模式：集合元素作为重复的同名查询参数（如 ?ids=1&amp;ids=2&amp;ids=3）。此为默认行为。
    /// </summary>
    Multi = 0,

    /// <summary>
    /// 逗号分隔模式：集合元素以逗号连接为单个查询参数（如 ?ids=1,2,3）。
    /// </summary>
    Csv = 1,

    /// <summary>
    /// 空格分隔模式：集合元素以空格连接为单个查询参数（如 ?ids=1%202%203）。
    /// </summary>
    Ssv = 2,

    /// <summary>
    /// 制表符分隔模式：集合元素以制表符连接为单个查询参数（如 ?ids=1%092%093）。
    /// </summary>
    Tsv = 3,

    /// <summary>
    /// 管道分隔模式：集合元素以管道符连接为单个查询参数（如 ?ids=1|2|3）。
    /// </summary>
    Pipes = 4
}
